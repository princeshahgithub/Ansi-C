//  ARITHMETIC OPERATORS ON FLOAT DATATYPE
//  Arithmetic operators are used to perform mathematical operations on numbers.
//  They are used to add, subtract, multiply, and divide numbers.



#include <stdio.h>

int main()
{
    float a = 4;
    float b = 5;
    printf("%f\n",a+b);
    printf("%f\n",a-b);
    printf("%f\n",a*b);
    printf("%f\n",a/b);
    return 0;

}



