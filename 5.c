//  ARITHMETIC OPERATORS ON INT DATATYPE
//  Arithmetic operators are used to perform mathematical operations on numbers.
//  They are used to add, subtract, multiply, and divide numbers.


#include <stdio.h>

int main()
{
    int a = 4;
    int b = 5;
    printf("%d\n",a+b);
    printf("%d\n",a-b);
    printf("%d\n",a*b);
    printf("%d\n",a/b);
    return 0;

}


